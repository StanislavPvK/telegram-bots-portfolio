from aiogram import Router, F, types

from app.database import get_tasks
from app.keyboards.inline_kb import inline_kb


# –°–æ–∑–¥–∞—ë–º —Ä–æ—É—Ç–µ—Ä –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ –∫–æ–º–∞–Ω–¥—ã "–°–ø–∏—Å–æ–∫ –∑–∞–¥–∞—á"
list_tasks_router = Router()


@list_tasks_router.message(F.text == ("–°–ø–∏—Å–æ–∫ –∑–∞–¥–∞—á üìú"))
async def list_tasks(message: types.Message):
    """
    –û–±—Ä–∞–±–æ—Ç—á–∏–∫ —Å–æ–æ–±—â–µ–Ω–∏—è "–°–ø–∏—Å–æ–∫ –∑–∞–¥–∞—á üìú".
    –ü–æ–ª—É—á–∞–µ—Ç –≤—Å–µ –∑–∞–¥–∞—á–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏–∑ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö –∏ –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –∏—Ö
    —Å inline-–∫–ª–∞–≤–∏–∞—Ç—É—Ä–æ–π –¥–ª—è –¥–µ–π—Å—Ç–≤–∏–π —Å –∑–∞–¥–∞—á–∞–º–∏.
    """

    # –ü–æ–ª—É—á–∞–µ–º –≤—Å–µ –∑–∞–¥–∞—á–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏–∑ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö
    tasks = await get_tasks(message.from_user.id)
   
    # –ï—Å–ª–∏ –∑–∞–¥–∞—á –Ω–µ—Ç ‚Äî –∏–Ω—Ñ–æ—Ä–º–∏—Ä—É–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    if not tasks:
        await message.answer("üì≠ –£ –≤–∞—Å –ø–æ–∫–∞ –Ω–µ—Ç –∑–∞–¥–∞—á.")
        return
    
    # –§–æ—Ä–º–∏—Ä—É–µ–º —Ç–µ–∫—Å—Ç–æ–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ —Å–æ —Å–ø–∏—Å–∫–æ–º –∑–∞–¥–∞—á
    text = "üìù –í–∞—à–∏ –∑–∞–¥–∞—á–∏:\n\n" + "\n".join(
    f"‚Ä¢ {task_text} ‚Äî {status}" 
    for _, task_text, status in tasks)

    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ —Å inline-–∫–ª–∞–≤–∏–∞—Ç—É—Ä–æ–π –¥–ª—è —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è –∑–∞–¥–∞—á–∞–º–∏
    await message.answer(text, reply_markup=inline_kb)